package variableName;

public class EscapeExample {
	public static void main(String[] args) {
		System.out.println("'Hello'");
		System.out.println("\"Hello\"");
		System.out.println("\\hello\\");
		System.out.println("수평탭\t수평탭");
		
		System.out.println("=========구분선=========");
		System.out.println("hihihi        \rhello");
		
		System.out.println("=========구분선=========");
		System.out.println("hihihi        \nhello");
		
		System.out.println("리턴문자\rHello");
		System.out.println("라인피드문자\nHello");
	}
}
