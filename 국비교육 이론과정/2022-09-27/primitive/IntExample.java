package primitive;

public class IntExample {
	public static void main(String[] args) {
		int var1 = 0b1010; // 2진수
		int var2 = 012; // 8진수
		int var3 = 10; // 10진수
		int var4 = 0xA; // 16진수
		
		System.out.println(var1);
		System.out.println(var2);
		System.out.println(var3);
		System.out.println(var4);
		
	}
}
