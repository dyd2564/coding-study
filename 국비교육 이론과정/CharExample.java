package primitive;

public class CharExample {
	public static void main(String[] args) {
		char c1 = 'A';
		System.out.println(c1);
		char c2 = 65;
		System.out.println(c2);
		char c3 = '\u0041';
		System.out.println(c3);
		
		int uniCode = c1;
		System.out.println(uniCode);
		
		char ch = '°¡';
		int uniCodeCh = ch;
		System.out.println(uniCodeCh);
	}
}
